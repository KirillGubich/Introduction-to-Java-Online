package by.jonline.cycle.main;

import java.util.Scanner;

public class Task1 {
	
	public static void main(String[] args) {
		// Ввод данных
        Scanner numInput = new Scanner(System.in);
        int num = numInput.nextInt();

        // Расчёты
        int sum = 0;
        for (int i = 1; i <= num; i++)
            sum += i;

        System.out.println(sum);
	}
}